    private void updateCheckboxState() {
        int selectedCount = 0;

        for (JCheckBox checkbox : checkboxList) {
            if (checkbox.isSelected()) {
                selectedCount++;
            }
        }

        for (JCheckBox checkbox : checkboxList) {
            if (!checkbox.isSelected() && selectedCount >= limit) {
                checkbox.setEnabled(false);
            } else {
                checkbox.setEnabled(true);
            }
        }
    }
